/* Zero-dependency exports — real CSV, Excel-openable .xls (HTML table), and a
   styled print-to-PDF window. Attaches to window.EXPORT. No CDN libraries, so
   the static site stays self-contained. */
(function () {
  function download(filename, mime, content) {
    var blob = new Blob([content], { type: mime });
    var url = URL.createObjectURL(blob), a = document.createElement("a");
    a.href = url; a.download = filename; document.body.appendChild(a); a.click();
    a.remove(); setTimeout(function () { URL.revokeObjectURL(url); }, 1000);
  }
  function csvEsc(v) { v = v == null ? "" : String(v); return /[",\n\r]/.test(v) ? '"' + v.replace(/"/g, '""') + '"' : v; }
  function htmlEsc(v) { return String(v == null ? "" : v).replace(/[&<>]/g, function (c) { return { "&": "&amp;", "<": "&lt;", ">": "&gt;" }[c]; }); }
  function stamp() { return window.APP && window.APP.fmtTs ? window.APP.fmtTs(new Date()) : ""; }
  function log(kind, name) { if (window.APP && window.APP.auditLog) window.APP.auditLog("EXPORT", kind + " export · " + name); }

  function csv(name, headers, rows) {
    var lines = [headers.map(csvEsc).join(",")].concat(rows.map(function (r) { return r.map(csvEsc).join(","); }));
    download(name + ".csv", "text/csv;charset=utf-8;", "﻿" + lines.join("\r\n"));
    log("CSV", name);
  }
  // An HTML-table workbook — Excel opens this as a native worksheet, no library needed.
  function xls(name, sheet, headers, rows) {
    var thead = "<tr>" + headers.map(function (h) { return '<th style="background:#10243b;color:#ffffff;padding:5px 8px;text-align:left">' + htmlEsc(h) + "</th>"; }).join("") + "</tr>";
    var tbody = rows.map(function (r) { return "<tr>" + r.map(function (c) { return '<td style="padding:4px 8px;border:0.5px solid #cccccc">' + htmlEsc(c) + "</td>"; }).join("") + "</tr>"; }).join("");
    var xml = '<!--[if gte mso 9]><xml><x:ExcelWorkbook><x:ExcelWorksheets><x:ExcelWorksheet><x:Name>' + htmlEsc(sheet || "Export") + '</x:Name><x:WorksheetOptions><x:DisplayGridlines/></x:WorksheetOptions></x:ExcelWorksheet></x:ExcelWorksheets></x:ExcelWorkbook></xml><![endif]-->';
    var html = '<html xmlns:o="urn:schemas-microsoft-com:office:office" xmlns:x="urn:schemas-microsoft-com:office:excel"><head><meta charset="utf-8">' + xml + "</head><body><table border='1' cellspacing='0'>" + thead + tbody + "</table></body></html>";
    download(name + ".xls", "application/vnd.ms-excel", html);
    log("Excel", name);
  }
  // A print-optimized window; the user saves as PDF from the print dialog.
  function pdf(title, bodyHtml) {
    var w = window.open("", "_blank");
    if (!w) { toast("Allow pop-ups to export a PDF"); return; }
    var css = "body{font-family:-apple-system,Segoe UI,Roboto,Helvetica,Arial,sans-serif;color:#10243b;margin:34px;font-size:12px;line-height:1.5}" +
      "h1{font-size:18px;margin:0 0 2px}h2{font-size:13px;margin:16px 0 6px}.sub{color:#5f6b7a;font-size:11px;margin-bottom:14px}" +
      "table{border-collapse:collapse;width:100%;margin:6px 0}th{background:#10243b;color:#fff;text-align:left;padding:5px 8px;font-size:10.5px}" +
      "td{padding:4px 8px;border-bottom:0.5px solid #e3e8ee;font-size:11px}.card{border:0.5px solid #e3e8ee;border-radius:8px;padding:12px 14px;margin:10px 0}" +
      ".kv{display:flex;gap:22px;flex-wrap:wrap;margin:6px 0}.kv div span{display:block}.kv .l{color:#5f6b7a;font-size:10px}.kv .v{font-weight:600;font-size:15px}" +
      ".foot{margin-top:22px;color:#8a95a3;font-size:10px;border-top:0.5px solid #e3e8ee;padding-top:8px}.pill{display:inline-block;padding:1px 8px;border-radius:999px;font-size:10px;background:#eef1f5}" +
      "@media print{.noprint{display:none}}";
    w.document.write("<html><head><title>" + htmlEsc(title) + "</title><style>" + css + "</style></head><body>" +
      "<h1>" + htmlEsc(title) + "</h1><div class='sub'>VA PIVOT — Payment Integrity Validation &amp; Oversight · Synthetic data, for demonstration only · " + htmlEsc(stamp()) + "</div>" +
      bodyHtml +
      "<div class='foot'>Generated by PIVOT. NPIs are invalid by construction; TINs use the 00- prefix. No real Veterans, providers, or claims.</div>" +
      "<div class='noprint' style='margin-top:16px'><button onclick='window.print()' style='padding:7px 14px;border-radius:7px;border:1px solid #10243b;background:#10243b;color:#fff;cursor:pointer'>Print / Save as PDF</button></div>" +
      "<scr" + "ipt>window.onload=function(){setTimeout(function(){window.print();},300);};</scr" + "ipt></body></html>");
    w.document.close();
    log("PDF", title);
  }

  // helpers for PDF bodies
  function tableHtml(headers, rows) {
    return "<table><thead><tr>" + headers.map(function (h) { return "<th>" + htmlEsc(h) + "</th>"; }).join("") + "</tr></thead><tbody>" +
      rows.map(function (r) { return "<tr>" + r.map(function (c) { return "<td>" + htmlEsc(c) + "</td>"; }).join("") + "</tr>"; }).join("") + "</tbody></table>";
  }
  function kvHtml(pairs) { return '<div class="kv">' + pairs.map(function (p) { return "<div><span class='l'>" + htmlEsc(p[0]) + "</span><span class='v'>" + htmlEsc(p[1]) + "</span></div>"; }).join("") + "</div>"; }

  // a reusable CSV / Excel / PDF button group + wiring
  function group(prefix) {
    return '<div style="display:flex;gap:6px" id="' + prefix + '-exp"><button class="btn" data-x="csv"><i class="ti ti-file-spreadsheet"></i> CSV</button><button class="btn" data-x="xls"><i class="ti ti-file-download"></i> Excel</button><button class="btn" data-x="pdf"><i class="ti ti-file-text"></i> PDF</button></div>';
  }
  function wire(prefix, handlers) {
    var el = document.getElementById(prefix + "-exp"); if (!el) return;
    el.querySelectorAll("button[data-x]").forEach(function (b) { b.onclick = function () { var fn = handlers[b.getAttribute("data-x")]; if (fn) fn(); }; });
  }

  function toast(msg) {
    var n = document.createElement("div"); n.textContent = msg;
    n.style.cssText = "position:fixed;bottom:20px;left:50%;transform:translateX(-50%);background:#10243b;color:#fff;padding:9px 16px;border-radius:8px;font-size:12.5px;z-index:300";
    document.body.appendChild(n); setTimeout(function () { n.remove(); }, 2400);
  }

  window.EXPORT = { csv: csv, xls: xls, pdf: pdf, group: group, wire: wire, tableHtml: tableHtml, kvHtml: kvHtml, htmlEsc: htmlEsc, toast: toast };
})();
